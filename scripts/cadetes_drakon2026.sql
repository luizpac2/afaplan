-- ── Tabela ──────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.cadetes (
  id            TEXT PRIMARY KEY,          -- ex: '26-001'
  nome_guerra   TEXT NOT NULL,
  nome_completo TEXT NOT NULL,
  quadro        TEXT NOT NULL,             -- CFOAV | CFOINT | CFOINF
  turma_aula    TEXT NOT NULL,             -- TURMA_A … TURMA_F
  cohort_id     TEXT REFERENCES public.cohorts(id),
  situacao      TEXT NOT NULL DEFAULT 'ATIVO',  -- ATIVO | DESLIGADO | TRANCADO | TRANSFERIDO
  observacao    TEXT,
  created_at    TIMESTAMPTZ DEFAULT now(),
  updated_at    TIMESTAMPTZ DEFAULT now()
);

ALTER TABLE public.cadetes ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "auth_read"  ON public.cadetes;
DROP POLICY IF EXISTS "auth_write" ON public.cadetes;

CREATE POLICY "auth_read"  ON public.cadetes FOR SELECT TO authenticated USING (true);
CREATE POLICY "auth_write" ON public.cadetes FOR ALL    TO authenticated USING (true) WITH CHECK (true);

GRANT SELECT, INSERT, UPDATE, DELETE ON public.cadetes TO authenticated;

